/**
 * Core metric data interface
 */
export interface Metric {
  id: string;
  businessId: string;
  pipelineId?: string;

  // Identification
  metricType: MetricType;
  metricKey: string;
  metricLabel: string;

  // Values
  currentValue: number;
  previousValue?: number;
  targetValue?: number;
  periodStartDate: Date;
  periodEndDate: Date;

  // Metadata
  unit: string;
  trend: Trend;
  trendPercentage?: number;

  // Timestamps
  createdAt: Date;
  updatedAt: Date;
}

/**
 * Metric display card data
 */
export interface MetricCard extends Metric {
  sparklineData?: Array<{ date: Date; value: number }>;
  isGood?: boolean;
  isWarning?: boolean;
}

// ═════════════════════════════════════════════════════════════════════════════
// INSIGHT TYPES
// ═════════════════════════════════════════════════════════════════════════════

/**
 * Supporting metrics for an insight
 */
export interface SupportingMetric {
  metricKey: string;
  label: string;
  value: number;
  unit: string;
  change?: number;
}

/**
 * Historical context for insights
 */
export interface InsightHistoricalContext {
  previousValue?: number;
  averageValue?: number;
  previousInsightDate?: Date;
  trendDuration?: string;
  pattern?: string;
}

/**
 * Base insight interface
 */
export interface BaseInsight {
  id: string;
  businessId: string;
  pipelineId?: string;
  insightType: InsightType;
  title: string;
  description: string;
  impactScore?: number;

  // AI metadata
  aiGenerated: boolean;
  aiModel?: string;
  confidenceScore?: number;

  // Data
  data: Record<string, unknown>;
  rootCause?: string;
  supportingMetrics?: SupportingMetric[];
  historicalContext?: InsightHistoricalContext;

  // User interaction
  dismissed: boolean;
  dismissedAt?: Date;
  dismissedBy?: string;
  actionTaken: boolean;
  actionTakenAt?: Date;
  actionDetails?: Record<string, unknown>;

  // Timestamps
  createdAt: Date;
  updatedAt: Date;
}

/**
 * Bottleneck insight - identifies stages where leads are stuck
 */
export interface BottleneckInsight extends BaseInsight {
  insightType: InsightType.BOTTLENECK;
  data: {
    stagedId: string;
    stageName: string;
    dealsStuck: number;
    avgDaysStuck: number;
    conversionRate: number;
    healthScore: number;
  };
}

/**
 * Performance insight - compares actual to target metrics
 */
export interface PerformanceInsight extends BaseInsight {
  insightType: InsightType.PERFORMANCE;
  data: {
    metricType: MetricType;
    actual: number;
    target: number;
    variance: number;
    variancePercent: number;
    period: string;
  };
}

/**
 * Forecast insight - predicts future performance
 */
export interface ForecastInsight extends BaseInsight {
  insightType: InsightType.FORECAST;
  data: {
    forecastType: ForecastType;
    predictedValue: number;
    lowerBound: number;
    upperBound: number;
    confidenceLevel: number;
    forecastPeriod: ForecastPeriod;
  };
}

/**
 * Anomaly insight - flags unusual patterns or values
 */
export interface AnomalyInsight extends BaseInsight {
  insightType: InsightType.ANOMALY;
  data: {
    anomalyType: AnomalyType;
    severity: Severity;
    baselineValue: number;
    detectedValue: number;
    deviationPercent: number;
    standardDeviations: number;
    likelyCauses: string[];
  };
}

/**
 * Trend insight - analyzes patterns over time
 */
export interface TrendInsight extends BaseInsight {
  insightType: InsightType.TREND;
  data: {
    metricType: MetricType;
    direction: Trend;
    percentChange: number;
    period: string;
    seasonalPattern?: string;
    forecastedDirection?: Trend;
  };
}

/**
 * Health insight - assesses overall business health
 */
export interface HealthInsight extends BaseInsight {
  insightType: InsightType.HEALTH;
  data: {
    overallHealthScore: number;
    scoreBreakdown: Record<string, number>;
    riskFactors: string[];
    opportunityFactors: string[];
  };
}

/**
 * Recommendation insight - suggests actions
 */
export interface RecommendationInsight extends BaseInsight {
  insightType: InsightType.RECOMMENDATION;
  data: {
    recommendedActions: string[];
    expectedOutcome: string;
    timelineForAction: string;
  };
}

/**
 * Discriminated union of all insight types
 */
export type Insight =
  | BottleneckInsight
  | PerformanceInsight
  | ForecastInsight
  | AnomalyInsight
  | TrendInsight
  | HealthInsight
  | RecommendationInsight;

