/**
 * Forecast actual values (populated after forecast period)
 */
export interface ForecastActual {
  value?: number;
  recordedAt?: Date;
  accuracyError?: number;
}

/**
 * Forecast interface
 */
export interface Forecast {
  id: string;
  businessId: string;
  pipelineId?: string;

  // Metadata
  forecastType: ForecastType;
  forecastPeriod: ForecastPeriod;

  // Window
  forecastStartDate: Date;
  forecastEndDate: Date;
  generatedAt: Date;

  // Values
  predictedValue: number;
  lowerBound?: number;
  upperBound?: number;
  confidenceLevel: number;

  // Model
  modelType: string;
  trainingDataPoints?: number;
  modelAccuracy?: number;

  // Actual (after forecast period ends)
  actual?: ForecastActual;

  // Factors
  keyFactors?: Record<string, unknown>;
  assumptions?: string;

  // Timestamps
  createdAt: Date;
  updatedAt: Date;
}

