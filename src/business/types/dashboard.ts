/**
 * Alert trigger condition
 */
export type AlertTriggerCondition =
  | 'above_threshold'
  | 'below_threshold'
  | 'equals'
  | 'deviates_by_percent'
  | 'deviates_by_std_dev';

/**
 * Alert notification method
 */
export type NotificationMethod = 'email' | 'in_app' | 'sms' | 'slack';

/**
 * Alert configuration interface
 */
export interface AlertConfiguration {
  id: string;
  businessId: string;

  // Configuration
  alertName: string;
  alertType: AnomalyType;
  metricType: string;
  description?: string;

  // Trigger
  triggerCondition: AlertTriggerCondition;
  thresholdValue?: number;
  deviationPercent?: number;
  stdDevCount?: number;

  // Severity
  severity: Severity;

  // Notifications
  notifyUsers?: string[];
  notificationMethods: NotificationMethod[];
  enableEmailNotifications: boolean;
  enableInAppNotifications: boolean;

  // Status
  enabled: boolean;
  createdBy?: string;

  // Timestamps
  createdAt: Date;
  updatedAt: Date;
}

