/**
 * Connect to MySQL and export schema + data for Supabase PostgreSQL migration.
 * Default: RedeemRocket and redeem_rocket_premium (Redeem Rocket app).
 *
 * Usage:
 *   npm run export:mysql
 *   node scripts/mysql-export-for-supabase.js
 *
 * Env (optional):
 *   MYSQL_DATABASES=RedeemRocket,redeem_rocket_premium  (default)
 *   Or single: MYSQL_DATABASE=employee_conduct
 *   MYSQL_HOST, MYSQL_USER, MYSQL_PASSWORD, MYSQL_PORT
 */

import 'dotenv/config';
import mysql from 'mysql2/promise';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const BASE_CONFIG = {
  host: process.env.MYSQL_HOST || 'hrms-db.comb9vkzwyzp.us-east-1.rds.amazonaws.com',
  user: process.env.MYSQL_USER || 'admin',
  password: process.env.MYSQL_PASSWORD || 'EIC12345',
  port: Number(process.env.MYSQL_PORT || '3306'),
  charset: 'utf8mb4',
};

const OUT_BASE = path.join(__dirname, 'mysql-export');

/** Which databases to export. Default: Redeem Rocket. */
function getDatabases() {
  if (process.env.MYSQL_DATABASE) return [process.env.MYSQL_DATABASE];
  const list = process.env.MYSQL_DATABASES || 'RedeemRocket,redeem_rocket_premium';
  return list.split(',').map((s) => s.trim()).filter(Boolean);
}

/** Map MySQL column type to PostgreSQL-friendly type for Supabase */
function mysqlTypeToPg(mysqlType) {
  const t = String(mysqlType).toLowerCase();
  if (t.includes('int') && !t.includes('bigint')) return 'integer';
  if (t.includes('bigint')) return 'bigint';
  if (t.includes('tinyint(1)') || t === 'tinyint') return 'boolean';
  if (t.includes('decimal') || t.includes('numeric') || t.includes('float') || t.includes('double')) return 'numeric';
  if (t.includes('datetime') || t.includes('timestamp')) return 'timestamptz';
  if (t.includes('date') && !t.includes('time')) return 'date';
  if (t.includes('text') || t.includes('longtext') || t.includes('mediumtext')) return 'text';
  if (t.includes('varchar') || t.includes('char(')) return 'text';
  if (t.includes('json')) return 'jsonb';
  if (t.includes('blob')) return 'bytea';
  return 'text';
}

async function exportDatabase(conn, database, outDir) {
  const [tables] = await conn.query(
    `SELECT table_name FROM information_schema.tables 
     WHERE table_schema = ? ORDER BY table_name`,
    [database]
  );
  const tableNames = tables.map((r) => r.TABLE_NAME);
  console.log(`\n[${database}] Tables: ${tableNames.length}`, tableNames.join(', ') || '(none)');

  if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });

  const schemaReport = { database, tables: {} };
  const allData = {};

  for (const table of tableNames) {
    const [columns] = await conn.query(
      `SELECT column_name, data_type, is_nullable, column_key, column_default, extra
       FROM information_schema.columns 
       WHERE table_schema = ? AND table_name = ? ORDER BY ordinal_position`,
      [database, table]
    );

    const pgColumns = columns.map((c) => ({
      name: c.COLUMN_NAME,
      mysqlType: c.DATA_TYPE,
      pgType: mysqlTypeToPg(c.DATA_TYPE),
      nullable: c.IS_NULLABLE === 'YES',
      key: c.COLUMN_KEY,
      default: c.COLUMN_DEFAULT,
      extra: c.EXTRA,
    }));

    schemaReport.tables[table] = { columns: pgColumns };

    const [rows] = await conn.query(`SELECT * FROM \`${database}\`.\`${table}\``);
    const serialized = (Array.isArray(rows) ? rows : []).map((row) => {
      const o = {};
      for (const k of Object.keys(row || {})) {
        const v = row[k];
        if (v instanceof Date) {
          const t = v.getTime();
          o[k] = Number.isFinite(t) ? v.toISOString() : null;
        } else if (Buffer.isBuffer(v)) o[k] = '[Buffer]';
        else o[k] = v;
      }
      return o;
    });
    const rowCount = serialized.length;
    console.log(`  ${table}: ${rowCount} rows`);

    allData[table] = serialized;
    fs.writeFileSync(
      path.join(outDir, `${table}.data.json`),
      JSON.stringify(serialized, null, 2),
      'utf8'
    );
  }

  fs.writeFileSync(
    path.join(outDir, 'schema.json'),
    JSON.stringify(schemaReport, null, 2),
    'utf8'
  );
  fs.writeFileSync(
    path.join(outDir, 'all-data.json'),
    JSON.stringify(allData, null, 2),
    'utf8'
  );

  const pgSql = [];
  pgSql.push(`-- ${database} → Supabase PostgreSQL`);
  pgSql.push('-- Review and run in Supabase SQL Editor');
  pgSql.push('');

  for (const table of tableNames) {
    const cols = schemaReport.tables[table].columns;
    const pk = cols.find((c) => c.key === 'PRI');
    const colDefs = cols.map((c) => {
      let def = `  "${c.name}" ${c.pgType}`;
      if (!c.nullable) def += ' NOT NULL';
      if (c.extra && String(c.extra).toLowerCase().includes('auto_increment')) def += ' GENERATED BY DEFAULT AS IDENTITY';
      return def;
    });
    pgSql.push(`CREATE TABLE IF NOT EXISTS "${table}" (`);
    const defs = pk ? [...colDefs, `  PRIMARY KEY ("${pk.name}")`] : colDefs;
    pgSql.push(defs.join(',\n'));
    pgSql.push(');');
    pgSql.push('');
  }

  fs.writeFileSync(path.join(outDir, 'schema-postgres.sql'), pgSql.join('\n'), 'utf8');
  return { database, tableCount: tableNames.length, outDir };
}

async function main() {
  const databases = getDatabases();
  console.log('MySQL export for Supabase', { host: BASE_CONFIG.host, databases });

  const conn = await mysql.createConnection({
    ...BASE_CONFIG,
    database: databases[0], // must connect to some DB; we use first, then switch via qualified names
  });

  try {
    const results = [];
    for (const db of databases) {
      const outDir = path.join(OUT_BASE, db);
      await exportDatabase(conn, db, outDir);
      results.push({ database: db, outDir });
    }

    console.log('\n--- Export complete ---');
    results.forEach(({ database, outDir }) => {
      console.log(`  ${database} → ${outDir}`);
    });
    console.log('  Files per DB: schema.json, schema-postgres.sql, all-data.json, <table>.data.json');
  } finally {
    await conn.end();
  }
}

main().catch((err) => {
  console.error('Error:', err.message);
  process.exit(1);
});
